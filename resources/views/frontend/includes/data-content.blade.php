<p align="center">
    <strong>Adatkezelési leírás</strong>
</p>
<p>
    A süti (cookie) egy kis fájl, amely akkor kerül a számítógépre, amikor Ön
    egy webhelyet látogat meg. A sütik számtalan funkcióval rendelkeznek.
    Többek között információt gyűjtenek, megjegyzik a látogató egyéni
    beállításait, felhasználásra kerülnek pl. az online bevásárlókosarak
    használatakor, és általánosságban megkönnyítik a weboldal használatát a
    felhasználók számára.
</p>
<p>
    <strong>
        A <u><a href="http://hazater.hu/">hazater.hu</a></u> milyen sütiket és
        mire használ?
    </strong>
</p>
<p>
    A <u><a href="http://hazater.hu/">hazater.hu</a></u> a sütiket a következő
    célokból használja:
</p>
<p>
    1. információ gyűjtése azzal kapcsolatban, hogyan használja Ön a weboldalt
    - annak felmérésével, hogy weboldalunk melyik részeit látogatja vagy
    használja leginkább, így megtudhatjuk, hogyan biztosítsunk Önnek még jobb
    felhasználói élményt, ha ismét meglátogatja oldalunkat,
</p>
<p>
    2. weboldalunk fejlesztése,
</p>
<p>
    3. az Ön navigációjának megkönnyítése weboldalunkon és annak funkcióinak
    használata során, így biztosítva a zökkenőmentes felhasználói élményt,
</p>
<p>
    4. célzott hirdetések elhelyezése más weboldalakon.
</p>
<p>
    <strong>Feltétlenül szükséges, munkamenet (session) sütik</strong>
</p>
<p>
    Ezek a sütik ahhoz szükségesek, hogy a felhasználók böngészhessék
    weboldalunkat, használják annak funkciót, pl. többek között az Ön által
    adott oldalakon végzett műveletek megjegyzését egy látogatás során. Ezen
    sütik érvényességi ideje kizárólag az Ön aktuális látogatására vonatkozik,
    a munkamenet végeztével, illetve a böngésző bezárásával a sütik e fajtája
    automatikusan törlődik a számítógépéről.
</p>
<p>
    Ne feledje, ezen cookie-k alkalmazása nélkül nem tudjuk garantálni Önnek
    weboldalunk használatát.
</p>
<p>
    <strong>Használatot elősegítő sütik</strong>
</p>
<p>
    Ezek lehetőséget biztosítanak számunkra arra, hogy megjegyezhessük a
    weboldalunkkal kapcsolatos választásait, vagy, hogy Ön a hagyományos vagy a
    vakok- és gyengén látóknak szánt oldalainkat látogatja.
</p>
<p>
    <strong>Teljesítményt biztosító sütik</strong>
</p>
<p>
    A Google Analytics sütiket arra használjuk, hogy információt gyűjtsünk
    azzal kapcsolatban, hogyan használják látogatóink weboldalunkat. Ezek a
    sütik nem tudják Önt személy szerint beazonosítani (az éppen használt IP
    címet is csak részben rögzítik), olyan információkat gyűjtenek, mint pl.
    hogy melyik oldalt nézett meg a látogatónk, a felhasználó a weboldal mely
    részére kattintott, hány oldalt keresett fel, milyen hosszú volt az egyes
    munkamenetek megtekintési ideje, melyek voltak az esetleges hibaüzenetek -
    mindezt weboldalunk fejlesztésének, valamint a felhasználók számára
    biztosított élmények javításának céljával.
</p>
<p>
    <strong>
        Hogyan ellenőrizheti és hogyan tudja kikapcsolnia a sütiket?
    </strong>
</p>
<p>
    Minden modern böngésző engedélyezi a sütik beállításának a változtatását. A
    legtöbb böngésző alapértelmezettként automatikusan elfogadja a sütiket, de
    ezek általában megváltoztathatóak, hogy megakadályozza az automatikus
    elfogadást és minden alkalommal felajánlja a választás lehetőségét, hogy
    szeretne vagy nem sütiket engedélyezni.
</p>
<p>
    Felhívjuk figyelmét, hogy mivel a sütik célja weboldalunk
    használhatóságának és folyamatainak megkönnyítése vagy lehetővé tétele, a
    cookie-k alkalmazásának megakadályozása vagy törlése által előfordulhat,
    hogy felhasználóink nem lesznek képesek weboldalunk funkcióinak teljes körű
    használatára, illetve hogy a weboldal a tervezettől eltérően fog működni
    böngészőjében.
</p>