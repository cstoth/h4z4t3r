    <strong>
        „Lépés-Váltás az Észak-Hegyháton” HazaTér közösségi közlekedés támogató rendszer
    </strong>
</p>
<p align="center">
    <strong>Általános Felhasználási feltételek</strong>
</p>
<p align="center">
    <strong>Felhasználói útmutató</strong>
</p>
<p>
    <u><strong>Utasként</strong></u>
</p>
<p>
    <strong>Hogyan kell autóst keresni?</strong>
</p>
<p>
    Ha utat keres, használja a főoldalon található keresőt: írja be az
    indulási- és érkezési címek nevét, majd pontosítsa a dátumot.
    <strong>
        A gépelési hibák elkerülése érdekében kérjük, válasszon az önműködően
        felkínált címek közül.
    </strong>
</p>
<p>
    Ezután a “Keresés” gombra kattintva az összes ajánlatot látni fogja, ami az
    adott pillanatban elérhető.
</p>
<p>
    <strong>Hogyan kell lefoglalni az utat?</strong>
</p>
<p>
    Miután a kereséskor megtalálta a megfelelő utat, tájékozódhat az utazás
    részleteiről. „Helyfoglalás” gomb megnyomásával a rendszer e-mailben jelzi
    az autós felé az utazási szándékot. Ezután feltudják egymással venni a
    kapcsolatot és egyeztetni az utazás részleteit.
</p>
<p>
    <u><strong>Autósként</strong></u>
</p>
<p>
    <strong>Hogyan tudja meghirdetni szabad üléseit?</strong>
</p>
<p>
    <strong>
        A főoldali menü „Feltöltök utat” gombra kattintva, a következő
        lépéseket követve regisztrálhatja a felajánlott utat, megadhatja annak
        részleteit.
    </strong>
</p>
<ol>
    <li>
        <p>
            Amennyiben még nem tette meg, regisztráljon egy gépjárművet a
            „Járműveim” menüpont alatt, nélküle nem lehet hirdetést feladni.
        </p>
    </li>
</ol>
<ol start="2">
    <li>
        <p>
            Amennyiben már regisztrált járművet, a „Hirdetés feladás” fül alatt
            meghirdetheti az útját.
        </p>
    </li>
</ol>
<ol start="3">
    <li>
        <p>
            Válassza ki a <strong>1. Jármű adatait (</strong>rendszám, szabad
            ülések száma)
        </p>
    </li>
</ol>
<ol start="4">
    <li>
        <p>
            A <strong>2.</strong> <strong>Útvonal, dátum megadása </strong>
            menüpontnál válasza ki az indulási és érkezési címeket, valamint az
            utazás dátumát. Az interaktív térképen rögtön ellenőrizni tudja a
            megadott útvonal helyességét.
        </p>
    </li>
</ol>
<ul>
    <li>
        <p>
Választhat egy útra és visszaútra is,            <strong>fontos hogy a pontos időpontot adja meg.</strong>
        </p>
    </li>
</ul>
<ul>
    <li>
        <p>
            A „köztes megálló hozzáadása”-val plusz megállóhelyeket adhat meg
            az indulás és az érkezési pontok között.
        </p>
    </li>
</ul>
<ul>
    <li>
        <p>
            A „Megjegyzés” mezőbe plusz információkat adhat meg az útról.
            Érdemes minél több részletet megadni. (pl. pontos indulási hely,
            autó ismertető stb.)
        </p>
    </li>
</ul>
<ol start="5">
    <li>
        <p>
            <strong>Mikor utazom?</strong>
            Rendszeres út megadásánál a felkínált rádió gombot válassza.
            Fontos, hogy pontosan határozza meg azokat a napokat, amelyeken
            utast kíván szállítani. Ha ezt nem teszi meg, nem jelenik meg a
            keresőben a felkínált út.
        </p>
    </li>
</ol>
<p>
    Amennyiben egyetlen alkalomra kínál fel utazást, ezt az „Egyedi gyakoriságú
    út(ak)” rádió gombra kattintva teheti meg. Itt konkrét dátumot megadva
    (év-hónap-nap) adott napon jelenik meg a hirdetése.
</p>
<ol start="6">
    <li>
        <p>
            Az „Elmentem sablonként” opció segítségével egy nevet megadva
            elmenthető az adott út, hogy a későbbiekben újra egyszerűen
            előhívható legyen.
        </p>
    </li>
</ol>
<ol start="7">
    <li>
        <p>
            A „Hirdetés feladása” gomb megnyomásával küldi el a rendszer a
            hirdetést, amely megjelenik a weboldalon. Az út a beállított
            időpontban azonnal láthatóvá válik minden tag számára, akik máris
            lefoglalhatják a meghirdetett üléseket, elküldhetik foglalási
            kérelmeiket.
        </p>
    </li>
</ol>
