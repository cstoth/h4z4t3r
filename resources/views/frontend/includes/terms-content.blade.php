            reklámozása.
        </p>
    </li>
</ol>
<p>
    <em><strong>Az Autósok kötelességei</strong></em>
</p>
<ol>
    <li>
        <p align="justify">
            Az Autós az utazás költségeinek megosztása érdekében nem kérhet
            semmilyen anyagi ellenszolgáltatást az általa szállított Utastól.
            Az Autós által bejelentett indulás helye és ideje kötelező jellegű.
            Amennyiben az Autós valamely okból mégsem tudja az utast a
            megajánlott helyre szállítani, köteles az Utas által megadott
            elérhetőségeken arról őt tájékoztatni. Az utazás elmaradásából
            származó károk tekintetében az oldal üzemeltetője mindennemű
            felelősséget kizár.
        </p>
    </li>
    <li>
        <p align="justify">
            Az Autós köteles az oldalon megadott adatait aktuálisan tartani.
            Amennyiben az Autós valótlan adatokkal szerepeltet, az abból erdő
            esetleges károk tekintetében az oldal üzemeltetője mindennemű
            felelősséget kizár.
        </p>
    </li>
    <li>
        <p align="justify">
            Az Autós nem köteles az Utasra a megbeszélt találkozási helyen 10
            percnél tovább várakozni
        </p>
    </li>
    <li>
        <p align="justify">
            Az Autós köteles az oldalon regisztrált gépjárművét folyamatosan az
            Utasok szállítására és biztonságos közlekedésre alkalmas, műszaki
            állapotban tartani. Az Autós köteles körültekintően és alaposan
            meggyőződni róla, hogy a gépjármű jogilag és műszakilag – a
            mindenkor hatályos előírásoknak megfelelőn – minden szempontból
            alkalmas a forgalomban való részvételre. (például, de nem
            kizárólag: érvényes forgalmi engedéllyel és kötelező biztosítással
            rendelkezik a jármű, az Autós érvényes vezetői engedéllyel
            rendelkezzen a járatot bonyolító járműre, stb.)
        </p>
    </li>
    <li>
        <p align="justify">
            Az Autós köteles az indulás helyén gépjárművezetésre alkalmas
            állapotban megjelenni, az utazás időtartama alatt teljes
            felelősséggel tartozik az Utas biztonságáért.
        </p>
    </li>
    <li>
        <p align="justify">
            Az Autós gépjárművének állapotából valamint az Autós állapotából
            eredő esetleges károkért az Üzemeltető semmilyen felelősséggel nem
            tartozik.
        </p>
    </li>
</ol>
