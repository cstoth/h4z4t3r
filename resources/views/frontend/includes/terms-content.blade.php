<p align="center">
    <strong>
        „Lépés-Váltás az Észak-Hegyháton” HazaTér közösségi közlekedés támogató rendszer
    </strong>
</p>
<p align="center">
    <strong>Általános Felhasználási feltételek</strong>
</p>
<p align="justify">
    A <u><a href="http://hazater.hu/">http://hazater.hu</a></u> web oldal
    tulajdonosa és üzemeltetője Alsómocsolád Község Önkormányzata. Az oldal
    létrehozásának célja, egy közösségi interaktív webes kommunikációs felület
    fejlesztése. A HazaTér interaktív közösségi közlekedés-támogató rendszer
    segítségével az egyéni és közösségi közlekedés térségi összehangolása
    valósul meg, ezáltal a szolgáltatás színvonalát, a Falubusz kihasználtságot
    és az utazási élményt az utazási költségek csökkentése mellett növelje az
    információs csatornák innovatív használatát. Valamint, hogy megtalálják
    egymást azok az alsómocsoládi lakosok, akik autójuk szabad kapacitását
    ellenszolgáltatás nélkül felajánlanák és azok, akik ezen szabad kapacitást
    ellenszolgáltatás nélkül igénybe vennék. A honlap és a rendszer igénybe
    vétele ingyenes.
</p>
<p align="justify">
Jelen Általános Felhasználási Feltételek    <u><a href="http://hazater.hu/">http://hazater.hu</a></u> weboldalon
    keresztül elérhető “HazaTér” közösségi közlekedés támogató rendszer
    igénybevételének és Felhasználók általi használatának feltételeit mutatja
    be.
</p>
<p align="justify">
    <strong>Az ÁFF-ben használat fogalmak:</strong>
</p>
<p align="justify">
    Honlap: <u><a href="http://hazater.hu/">http://hazater.hu</a></u>
</p>
<p align="justify">
    Üzemeltető: Alsómocsolád Község Önkormányzata
</p>
<p align="justify">
    Rendszer: Az informatikai rendszer valamint a használatát lehetővé tevő
    Honlap
</p>
<p align="justify">
    Autós: Olyan regisztrált felhasználó, aki meghirdeti szabad helyeit (autós
    hirdetést ad fel)
</p>
<p align="justify">
    Utas: olyan regisztrált felhasználó, aki helyet foglal valamelyik Autósnál
</p>
<p align="justify">
    Felhasználó: a Rendszerben regisztrált személy, Autós vagy/és Utas
</p>
<p align="justify">
    Regisztráció: a Rendszerben a “Regisztráció” menüpont alatt található
    adatok kitöltése, majd az Üzemeltetőnek történő továbbítása a
    “Regisztráció” gombra történő kattintással vagy a „Regisztráció Facebook
    fiókkal” gombra történő kattintás
</p>
<p align="justify">
    E-mail cím: a Felhasználó által a Regisztráció során megadott e-mail címe,
    mely egyedi azonosításra szolgál, a Felhasználót ez különbözteti meg
    egyedileg a Rendszerben.
</p>
<p align="justify">
    Mobilszám: a Felhasználó által megadott telefonszám, mely a
    kapcsolatfelvételre szolgál Autós és Utas között.
</p>
<p>
    Profillap: A felhasználóval kapcsolatos információk
</p>
<p align="justify">
    Üzemeltető jogosult jelen ÁFF-et a Rendszer Felhasználóinak előzetes
    tájékoztatása mellett, egyoldalúan is módosítani. A tájékoztatás a Honlapon
    való közzététellel valósul meg, a módosított ÁFF hatálybalépését megelőzően
    legalább 3 nappal. A Felhasználóval szemben a módosított rendelkezések a
    hatályba lépést követően a Rendszer első használata alkalmával válnak
    hatályossá. Amennyiben a Felhasználó az ÁFF módosításaival nem ért egyet,
    regisztrációjának törlésére van lehetőség.
</p>
<p>
    <strong>Adatkezelés, adattovábbítás: </strong>
</p>
<p align="justify">
    1. Rendszer a Felhasználók személyes adatait (beleértve az IP címet)
    bizalmasan kezeli, az adattovábbítás alpontban felsorolt eseteken kívül
    harmadik félnek nem szolgáltatja ki, és garantálja a törvényes adatvédelem
    betartását. Az adatok tovább-szolgáltatása rendőrség illetve jogvédő
    szervek felé megengedett, ha azok bűncselekmény alapos gyanúja miatt
    adatokat kérnek. A Rendszert teljes körűen csak a regisztrált Felhasználók
    használhatják. A Regisztrációval a Felhasználó elfogadja és magára nézve
    kötelező érvényűnek tekinti az ÁFF rendelkezéseit. Személyes adatok csak az
    Autós és Utas hirdetéseknél jelennek meg, ott is csak a regisztrált
    Felhasználók férhetnek hozzá. A Rendszerben regisztrált Felhasználók által
    elérhető információk továbbítása és jogszerűtlen felhasználása tilos, és a
    rendszer használatából történő kizárást vonja maga után!
</p>
<p>
    2. A regisztráció törlésére lehetőség van a Profil/Adatlap menüpont alatt.
    Ekkor a felhasználó adatai véglegesen törlődnek.
</p>
<p>
    <em><strong>Regisztráció</strong></em>
</p>
<p align="justify">
    A Rendszer bizonyos szolgáltatásai feliratkozás regisztráció nélkül is
    elérhetőek minden, a hatályos magyar jogszabályok szerint jog- és
    cselekvőképes egyén számára. Bizonyos szolgáltatások azonban
    regisztrációhoz, majd belépéshez kötöttek. A Rendszerbe történő
    regisztráció díjmentes.
</p>
<ol>
    <li>
        <p align="justify">
            Regisztrálni a Rendszer felhasználói felületeinek egyikén a
            “Regisztráció” menüpont alatt az adatmezők kitöltésével lehet. Ez
            utóbbi esetben a Facebook általános felhasználói feltételei is
            érvényesek. A regisztráció szükséges feltétele az ÁFF elfogadása.
        </p>
    </li>
    <li>
        <p align="justify">
            Az adatbeviteli hibákból, illetve az adatok szándékolt vagy csalárd
            módon történő téves megadásából eredő károkért és az adatbevitel
            közben esetlegesen felmerülő technikai problémákért az Üzemeltető
            felelősséget nem vállal.
        </p>
    </li>
</ol>
<p>
    <em><strong>Az oldal igénybevételének feltételei</strong></em>
</p>
<p align="justify">
    A Felhasználó kizárólag abban az esetben jogosult a Szolgáltatás
    igénybevételére, ha cselekvőképes személy, vagy korlátozottan cselekvőképes
    személy, vagy törvényes képviselője kifejezett engedélyével bír. A
    Regisztrációval a Felhasználó elismeri és szavatolja, hogy az ÁFF-ben
    foglaltak szerint jogosult a Rendszer igénybevételére.
</p>
