            tartozik felelősséggel. Felhasználó köteles gondoskodni arról, hogy
            az általa a Rendszerben elérhetővé tett tartalom harmadik személyek
            jogait vagy a hatályos, magyar jogszabályokat ne sértse.
        </p>
    </li>
    <li>
        <p align="justify">
            A Felhasználó maga felel a Rendszerben megadott adataiért,
